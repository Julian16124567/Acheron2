def animateText(text, amount):
    loading = ["", ".", "..", "..."] 
    color = "\033[95m"  
    reset = "\033[0m"
    for i in range(amount):
        for frame in loading:
            sys.stdout.write(f"\r{color}[*] {text}{frame}{reset}   ")
            sys.stdout.flush()
            time.sleep(0.4)
    print()

